package Variables;

public class VariableDeclarationAndInitialization {
	
	public static void main(String[] args) {
		byte bytvar = 110;
		System.out.println("the value of byte is:" + bytvar);
		int ivar = 120;
		System.out.println("the value of int is:" + ivar);
		short svar = 250;
		System.out.println("the value of short is:" + svar);
		long lvar = 10000L;
		System.out.println("the value of long is:" + lvar);
		double dvar = 250.456;
		System.out.println("the value of double is:" + dvar);
		float fvar= 250.45F;
		System.out.println("the value of float is:" + fvar);
		char cvar = 'b';
		System.out.println("the value of char is:" + cvar);
		boolean blvar = Boolean.FALSE;
		System.out.println("the value of boolean is:" + blvar);
		

}
}